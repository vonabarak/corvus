-- SSH key injection
--------------------------------------------------------------------------------

-- | Inject SSH public keys into a cloud-config YAML text.
-- Parses the YAML as a Value, merges keys into the first user's
-- @ssh_authorized_keys@ (or adds a top-level key), and re-serializes.
injectSshKeysIntoYaml :: Text -> [Text] -> Either Text Text
injectSshKeysIntoYaml yamlText sshPubKeys =
  case Yaml.decodeEither' (T.encodeUtf8 yamlText) :: Either Yaml.ParseException Value of
    Left err -> Left $ "Failed to parse custom user-data YAML: " <> T.pack (show err)
    Right val ->
      let merged = mergeKeysIntoValue val (map T.strip sshPubKeys)
       in Right $ T.decodeUtf8 $ Yaml.encode merged

-- | Merge SSH keys into a parsed cloud-config Value.
-- If 'users' array exists, inject into the first user's ssh_authorized_keys.
-- Otherwise, add a top-level ssh_authorized_keys key.
mergeKeysIntoValue :: Value -> [Text] -> Value
mergeKeysIntoValue (Object obj) keys =
  case KM.lookup (Key.fromText "users") obj of
    Just (Array users)
      | not (V.null users) ->
          let firstUser = V.head users
              updatedUser = injectKeysIntoUser firstUser keys
              updatedUsers = Array $ V.cons updatedUser (V.tail users)
           in Object $ KM.insert (Key.fromText "users") updatedUsers obj
    _ ->
      -- No users section: add top-level ssh_authorized_keys
      let keyList = Array $ V.fromList $ map (String . T.strip) keys
       in Object $ KM.insert (Key.fromText "ssh_authorized_keys") keyList obj
mergeKeysIntoValue val _ = val

-- | Inject SSH keys into a single user object's ssh_authorized_keys.
injectKeysIntoUser :: Value -> [Text] -> Value
injectKeysIntoUser (Object userObj) keys =
  let newKeys = map (String . T.strip) keys
      existingKeys = case KM.lookup (Key.fromText "ssh_authorized_keys") userObj of
        Just (Array arr) -> V.toList arr
        _ -> []
      mergedKeys = Array $ V.fromList $ existingKeys ++ newKeys
   in Object $ KM.insert (Key.fromText "ssh_authorized_keys") mergedKeys userObj
injectKeysIntoUser val _ = val
