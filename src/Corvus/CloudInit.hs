-- | Cloud-init configuration
data CloudInitConfig = CloudInitConfig
  { ciUser :: !Text
  -- ^ Username to create (used by default config)
  , ciHostname :: !Text
  -- ^ Hostname for the VM
  , ciInstanceId :: !Text
  -- ^ Instance ID (should be unique per VM)
  , ciCustomUserData :: !(Maybe Text)
  -- ^ Custom user-data YAML (replaces default generation when set)
  , ciNetworkConfig :: !(Maybe Text)
  -- ^ Network-config YAML (not generated by default)
  , ciInjectSshKeys :: !Bool
  -- ^ When True, inject DB SSH keys into custom userData
  }
  deriving (Show, Eq)

-- | Default cloud-init configuration
defaultCloudInitConfig :: CloudInitConfig
defaultCloudInitConfig =
  CloudInitConfig
    { ciUser = "corvus"
    , ciHostname = "corvus-vm"
    , ciInstanceId = "corvus-001"
    , ciCustomUserData = Nothing
    , ciNetworkConfig = Nothing
    , ciInjectSshKeys = True
    }

-- | Get the directory for storing cloud-init files for a VM
-- Returns: $qcBasePath/<vm-name>/
getCloudInitDir :: QemuConfig -> Text -> IO FilePath
getCloudInitDir config vmName = do
  basePath <- getEffectiveBasePath config
  let vmDir = basePath </> T.unpack vmName
  createDirectoryIfMissing True vmDir
  pure vmDir

-- | Get the path to the cloud-init ISO for a VM
-- Returns: $qcBasePath/<vm-name>/cloud-init.iso
getCloudInitIsoPath :: QemuConfig -> Text -> IO FilePath
getCloudInitIsoPath config vmName = do
  vmDir <- getCloudInitDir config vmName
  pure $ vmDir </> "cloud-init.iso"

-- | Generate user-data YAML for cloud-init with multiple SSH keys
generateUserData :: CloudInitConfig -> [Text] -> Text
generateUserData config sshPubKeys' =
  let sshPubKeys = map T.strip sshPubKeys'
      user = ciUser config
      home = "/home/" <> user
      runcmds =
        [ "chmod 700 " <> home
        , "chmod 700 " <> home <> "/.ssh"
        , "chmod 600 " <> home <> "/.ssh/authorized_keys"
        , "chown -R " <> user <> ":" <> user <> " " <> home <> "/.ssh"
        , "echo 'permit nopass " <> user <> "' > /etc/doas.d/" <> user <> ".conf 2>/dev/null || true"
        , "rc-service sshd restart || systemctl restart ssh || systemctl restart sshd || true"
        , "systemctl enable qemu-guest-agent || rc-update add qemu-guest-agent default || true"
        , "systemctl start qemu-guest-agent || rc-service qemu-guest-agent start || true"
        ]
   in "#cloud-config\n"
        <> T.decodeUtf8
          ( Yaml.encode
              [yamlQQ|
            ssh_genkeytypes:
              - rsa
              - ed25519
            bootcmd:
              - test -f /etc/ssh/ssh_host_rsa_key || ssh-keygen -t rsa -f /etc/ssh/ssh_host_rsa_key -N ''
              - test -f /etc/ssh/ssh_host_ed25519_key || ssh-keygen -t ed25519 -f /etc/ssh/ssh_host_ed25519_key -N ''
            chpasswd:
              expire: false
            users:
              - name: #{user}
                sudo: ALL=(ALL) NOPASSWD:ALL
                shell: /bin/sh
                lock_passwd: false
                plain_text_passwd: corvus
                ssh_authorized_keys: #{sshPubKeys}
            ssh_pwauth: true
            disable_root: true
            package_update: true
            package_upgrade: false
            packages:
              - qemu-guest-agent
            runcmd: #{runcmds}
          |]
          )

-- | Generate meta-data for cloud-init
generateMetaData :: CloudInitConfig -> Text
generateMetaData config =
  T.decodeUtf8 $
    Yaml.encode
      [yamlQQ|
        instance-id: #{ciInstanceId config}
        local-hostname: #{ciHostname config}
      |]

-- | Generate a cloud-init NoCloud ISO image with multiple SSH keys.
-- The ISO contains user-data, meta-data, and optionally network-config files.
-- Returns the path to the generated ISO, or an error message.
generateCloudInitIso :: FilePath -> CloudInitConfig -> [Text] -> IO (Either Text FilePath)
generateCloudInitIso targetDir config sshPubKeys = do
  -- Ensure target directory exists
  createDirectoryIfMissing True targetDir

  let userDataPath = targetDir </> "user-data"
      metaDataPath = targetDir </> "meta-data"
      networkConfigPath = targetDir </> "network-config"
      isoPath = targetDir </> "cloud-init.iso"

  -- Generate user-data: custom or default.
  -- Raw scripts (starting with #, e.g. #ps1_sysnative, #!, #cloud-config)
  -- are used as-is. Structured YAML gets a #cloud-config header prepended.
  let isRawScript t = T.isPrefixOf "#" (T.stripStart t)
      userDataContent = case ciCustomUserData config of
        Just customData
          | isRawScript customData -> customData
          | ciInjectSshKeys config && not (null sshPubKeys) ->
              case injectSshKeysIntoYaml customData sshPubKeys of
                Right merged -> "#cloud-config\n" <> merged
                Left _ -> "#cloud-config\n" <> customData
          | otherwise -> "#cloud-config\n" <> customData
        Nothing -> generateUserData config sshPubKeys

  -- Write cloud-init files
  TIO.writeFile userDataPath userDataContent
  TIO.writeFile metaDataPath (generateMetaData config)

  -- Write optional network-config
  mNetworkConfigFile <- case ciNetworkConfig config of
    Just nc -> do
      TIO.writeFile networkConfigPath nc
      pure $ Just networkConfigPath
    Nothing -> pure Nothing

  let inputFiles = [userDataPath, metaDataPath] ++ catMaybes [mNetworkConfigFile]

  -- Try genisoimage first, fall back to mkisofs
  result <- tryGenIsoImage inputFiles isoPath
  case result of
    Right _ -> do
      -- Clean up temp files
      mapM_ removeIfExists inputFiles
      pure $ Right isoPath
    Left _ -> do
      -- Try mkisofs as fallback
      mkResult <- tryMkIsofs inputFiles isoPath
      case mkResult of
        Right _ -> do
          mapM_ removeIfExists inputFiles
          pure $ Right isoPath
        Left err -> pure $ Left err

-- | Try to create ISO using genisoimage
tryGenIsoImage :: [FilePath] -> FilePath -> IO (Either Text ())
tryGenIsoImage inputFiles isoPath = do
  result <-
    try $
      readProcessWithExitCode
        "genisoimage"
        (["-output", isoPath, "-volid", "cidata", "-joliet", "-rock"] ++ inputFiles)
        ""
  case result of
    Left (_ :: SomeException) -> pure $ Left "genisoimage not found"
    Right (ExitSuccess, _, _) -> pure $ Right ()
    Right (ExitFailure n, _, stderr) ->
      pure $
        Left $
          "genisoimage failed (exit " <> T.pack (show n) <> "): " <> T.pack stderr

-- | Try to create ISO using mkisofs
tryMkIsofs :: [FilePath] -> FilePath -> IO (Either Text ())
tryMkIsofs inputFiles isoPath = do
  result <-
    try $
      readProcessWithExitCode
        "mkisofs"
        (["-output", isoPath, "-volid", "cidata", "-joliet", "-rock"] ++ inputFiles)
        ""
  case result of
    Left (_ :: SomeException) -> pure $ Left "mkisofs not found"
    Right (ExitSuccess, _, _) -> pure $ Right ()
    Right (ExitFailure n, _, stderr) ->
      pure $
        Left $
          "mkisofs failed (exit " <> T.pack (show n) <> "): " <> T.pack stderr

-- | Remove cloud-init ISO file
removeCloudInitIso :: FilePath -> IO ()
removeCloudInitIso = removeIfExists

-- | Helper to remove file if it exists
removeIfExists :: FilePath -> IO ()
removeIfExists path = do
  exists <- doesFileExist path
  when exists $ removeFile path

